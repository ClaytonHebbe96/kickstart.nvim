-- Move a block of code up/down
vim.keymap.set('v', 'K', ":m '<-2<CR>gv=gv")
vim.keymap.set('v', 'J', ":m '>+1<CR>gv=gv")

vim.keymap.set('n', 'J', 'mzJ`z')
vim.keymap.set('n', '<C-d>', '<C-d>zz')
vim.keymap.set('n', '<C-u>', '<C-u>zz')
vim.keymap.set('n', 'n', 'nzzzv')
vim.keymap.set('n', 'N', 'Nzzzv')

vim.keymap.set('x', '<leader>p', '"_dP')

vim.keymap.set('n', '<leader>y', '"+y')
vim.keymap.set('v', '<leader>y', '"+y')
vim.keymap.set('n', '<leader>Y', '"+Y')

vim.keymap.set('n', '<leader>d', '"_d')
vim.keymap.set('v', '<leader>d', '"_d')

vim.keymap.set('i', '<C-c>', '<Esc>')

vim.keymap.set('n', '<leader>f', function()
  vim.lsp.buf.format()
end)

-- INFO: setting keymaps to write and save all
-- as well as closing all nivm windows
vim.keymap.set('n', '<leader>w', ':w<CR>:so<CR>', { desc = 'Write and source' })
vim.keymap.set('n', '<leader>q', ':qa<CR>', { desc = 'Close all windows' })

-- INFO: remap command mode to semicolon(;)
-- also keep ; but swap it to the original color(<shift>;)
vim.keymap.set('n', ';', ':', { desc = 'simple command mode' })
vim.keymap.set('n', ':', ';', { desc = 'keep semicolon' })

